//Create class KmToMiles to convert distance from km to miles.
class KmToMiles{
	public static void main(String args[]){
		//Create a variable having the distance in km.
		double km=10.8;

		//Create a variable to store result.
		double distance=km*1.6;

		//Displaying the result.
		System.out.println("The distance "+km+" km in miles is "+distance);
	}
}
